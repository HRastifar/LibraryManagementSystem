    private void btnReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReturnActionPerformed
           String sql = "delete from issue_book where member_id=? and book_id=?";
        
        try(PreparedStatement pst = connection.prepareStatement(sql);)
        {
            pst.setString(1, txtMemberId.getText());
            pst.setString(2, txtBookId.getText());
           
           
            if(pst.execute()){
                JOptionPane.showOptionDialog(null, " Some thing went wrong !","Failure",JOptionPane.OK_CANCEL_OPTION,JOptionPane.INFORMATION_MESSAGE,null,null,null);
            }
                else{
               
                int input = JOptionPane.showOptionDialog(null, "  Returned successfully !","Success",JOptionPane.OK_CANCEL_OPTION,JOptionPane.INFORMATION_MESSAGE,null,null,null);
                if(input == JOptionPane.OK_OPTION){
                   clear();
                }
            }
            
            
        }catch(Exception e){
             JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_btnReturnActionPerformed

    private void btnReturn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReturn1ActionPerformed
        
              String sqlForDelay = "select * from issue_book where member_id=? and book_id=?";
        
        try(PreparedStatement pst = connection.prepareStatement(sqlForDelay);)
        {
            pst.setString(1, txtMemberId.getText());
            pst.setString(2, txtBookId.getText());
            try(ResultSet rs = pst.executeQuery()){
                if(rs.next()){
                    java.sql.Date returnDate = rs.getDate(4);
                    long days = calculateDelayedDays(returnDate,calculateCurrentDate());
                    txtDelay.setText(Long.toString(days));
                    txtFine.setText(Long.toString(days*finePrice));
                }
                else{
                   JOptionPane.showOptionDialog(null, " Some thing went wrong !","Failure",JOptionPane.OK_CANCEL_OPTION,JOptionPane.INFORMATION_MESSAGE,null,null,null); 
                }
            }
        }catch(Exception e){
             JOptionPane.showMessageDialog(null, e);
        }
       
    }//GEN-LAST:event_btnReturn1ActionPerformed
