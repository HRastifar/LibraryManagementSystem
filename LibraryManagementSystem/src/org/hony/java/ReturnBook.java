    private Date calculateCurrentDate(){
        long millis = System.currentTimeMillis();
        return new java.sql.Date(millis);
    }

    private long calculateDelayedDays(Date returnDate, Date calculateCurrentDate) {
       // To calculate the time difference of two dates 
         long differenceInTime = returnDate.getTime()- calculateCurrentDate.getTime(); 
       // To calculate the no. of days between two dates 
   
        long differenceIndays = differenceInTime/(1000*3600*24);
        if(differenceIndays <=0 )
            return 0;
        else
            return differenceIndays;
        
    }
